import usePoolRouter from "@/hooks/contracts/usePoolRouter";
import useBlockNumber from "@/hooks/useBlockNumber";
import { useEagerConnect } from "@/hooks/useEagerConnect";
import useWeb3Store from "@/hooks/useWeb3Store";
import useGetPoolTokens from "@/hooks/view/useGetPoolTokens";
import useTokenBalance from "@/hooks/view/useTokenBalance";
import { injected } from "@/lib/connectors/metamask";
import { parseUnits } from "@ethersproject/units";
import Head from "next/head";
import { FormEvent } from "react";

function Home() {
  useEagerConnect();

  const account = useWeb3Store((state) => state.account);

  async function connect() {
    try {
      await injected.activate();
    } catch (error) {
      console.error(error);
    }
  }

  const { data: poolTokens } = useGetPoolTokens();
  const { data: tokenBalance } = useTokenBalance(account);
  const { data: blockNumber } = useBlockNumber();

  const contract = usePoolRouter();

  async function deposit() {}

  async function onSubmit(event: FormEvent) {
    event.preventDefault();

    const values = event.target as typeof event.target & {
      token: { value: string };
      amount: { value: string };
      slippage: { value: string };
    };

    try {
      await contract.deposit(
        values.token.value,
        parseUnits(values.amount.value, "wei"),
        parseUnits("1", "wei"),
        1000
      );
    } catch (error) {
      console.error(error);
    }
  }

  return (
    <div className="p-5">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <nav className="mb-8">
        <div>
          {account ? (
            <ul>
              <li>
                <p>Connected Address</p>
                <p>{account}</p>
              </li>
              <li>
                <p>SOV Balance</p>
                <p>{tokenBalance?.toNumber()?.toFixed(4)}</p>
              </li>
              <li>
                <p>Block Number</p>
                <p>{blockNumber}</p>
              </li>
            </ul>
          ) : (
            <button onClick={connect}>Connect Wallet</button>
          )}
        </div>
      </nav>

      <main>
        <div>
          <h2 className="mb-4 text-lg font-bold">Deposit</h2>

          <form onSubmit={onSubmit} method="POST" className="space-y-4">
            <div>
              <label className="block" htmlFor="token">
                Select a token
              </label>

              <select name="token" id="token">
                <option value="">Select a token</option>
                {poolTokens?.map((token) => (
                  <option key={token.address} value={token.address}>
                    {token.name}
                  </option>
                ))}
              </select>
            </div>

            <div>
              <label className="block" htmlFor="amount">
                Enter amount of token
              </label>

              <input
                autoComplete="off"
                autoCorrect="off"
                inputMode="decimal"
                maxLength={79}
                minLength={1}
                name="amount"
                id="amount"
                pattern="^[0-9]*[.,]?[0-9]*$"
                placeholder="0.0"
                spellCheck="false"
                type="text"
              />
            </div>

            <div>
              <p>Advanced</p>

              <div>
                <label className="block" htmlFor="slippage">
                  Slippage Percent
                </label>

                <input
                  autoComplete="off"
                  autoCorrect="off"
                  inputMode="decimal"
                  id="slippage"
                  name="slippage"
                  maxLength={4}
                  minLength={1}
                  pattern="^[0-9]*[.,]?[0-9]*$"
                  placeholder="0.10"
                  spellCheck="false"
                  type="text"
                />

                <span>%</span>
              </div>
            </div>

            <button type="submit">Deposit</button>
          </form>
        </div>
      </main>
    </div>
  );
}

export default Home;
